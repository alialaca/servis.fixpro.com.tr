  KİŞİSEL VERİLERİN KORUNMASI KANUNU (KVKK) AYDINLATMA METNİ

  İşbu Aydınlatma Metni, 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) kapsamında, veri sorumlusu sıfatıyla [Firma Adı] tarafından kişisel verilerinizin işlenmesine ilişkin olarak sizleri bilgilendirmek amacıyla hazırlanmıştır.

  1. Veri Sorumlusunun Kimliği

  KVKK uyarınca, kişisel verileriniz, [Firma Adı] (“Şirket”) tarafından veri sorumlusu sıfatıyla işlenmektedir.

  2. Kişisel Verilerin Hangi Amaçla İşleneceği

  Toplanan kişisel verileriniz aşağıdaki amaçlarla işlenmektedir:
  •	Şirket faaliyetlerinin yürütülmesi
  •	Müşteri ilişkilerinin yönetimi
  •	Ürün ve hizmetlerin sunulması ve geliştirilmesi
  •	İletişim faaliyetlerinin yürütülmesi
  •	Yasal yükümlülüklerin yerine getirilmesi
  •	Talep ve şikayetlerin takibi
  •	Finans ve muhasebe işlemlerinin gerçekleştirilmesi

  3. İşlenen Kişisel Verilerin Kimlere ve Hangi Amaçla Aktarılabileceği

  Kişisel verileriniz, yukarıda belirtilen amaçlar doğrultusunda;
  •	Yasal zorunluluklar kapsamında yetkili kamu kurum ve kuruluşlarına,
  •	Hizmet alınan üçüncü taraflara (örneğin; kargo firmaları, yazılım hizmeti sağlayıcıları vb.),
  •	İş ortaklarımıza ve iştiraklerimize,
  •	Hukuki yükümlülükler çerçevesinde yetkili yargı mercilerine,
  aktarılabilecektir.

  4. Kişisel Veri Toplamanın Yöntemi ve Hukuki Sebebi

  Kişisel verileriniz, sözlü, yazılı ya da elektronik ortamda; web sitemiz, çağrı merkezimiz, e-posta, mobil uygulamalar ve benzeri kanallar aracılığıyla otomatik veya otomatik olmayan yollarla toplanmaktadır.

  Bu veriler, KVKK’nın 5. ve 6. maddelerinde belirtilen kişisel veri işleme şartları kapsamında toplanmakta ve işlenmektedir.

  5. KVKK’nın 11. Maddesi Kapsamındaki Haklarınız

  KVKK’nın 11. maddesi uyarınca, kişisel verilerinizle ilgili olarak aşağıdaki haklara sahipsiniz:
  •	Kişisel verilerinizin işlenip işlenmediğini öğrenme,
  •	Kişisel verileriniz işlenmişse buna ilişkin bilgi talep etme,
  •	Kişisel verilerinizin işlenme amacını ve bunların amacına uygun kullanılıp kullanılmadığını öğrenme,
  •	Yurt içinde veya yurt dışında kişisel verilerinizin aktarıldığı üçüncü kişileri bilme,
  •	Kişisel verilerinizin eksik veya yanlış işlenmiş olması hâlinde bunların düzeltilmesini isteme,
  •	KVKK’nın 7. maddesinde öngörülen şartlar çerçevesinde kişisel verilerinizin silinmesini veya yok edilmesini isteme,
  •	Düzeltme, silme veya yok edilme işlemlerinin, kişisel verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme,
  •	İşlenen verilerin münhasıran otomatik sistemler vasıtasıyla analiz edilmesi suretiyle aleyhinize bir sonucun ortaya çıkmasına itiraz etme,
  •	Kişisel verilerinizin kanuna aykırı olarak işlenmesi sebebiyle zarara uğramanız hâlinde zararın giderilmesini talep etme.

  6. İletişim

  KVKK kapsamındaki haklarınızı kullanmak için [iletisim@firmaadi.com] adresine yazılı olarak veya [adres] posta adresine başvurabilirsiniz.
